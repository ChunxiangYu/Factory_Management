public class factory {
    private int F_number;
    private double area;
    private String address;
    private String Last_Production_Time;
    private String product;
    public int getF_number() {
        return F_number;
    }

    public void setF_number(int f_number) {
        F_number = f_number;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getLast_Production_Time() {
        return Last_Production_Time;
    }

    public void setLast_Production_Time(String last_Production_Time) {
        Last_Production_Time = last_Production_Time;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }


}
